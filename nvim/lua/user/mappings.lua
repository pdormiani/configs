
-- Alt+[hjkl] to navigate through splits in terminal mode
tnoremap ([[<M-h>]], [[<C-\><C-n><C-w>h]])
tnoremap ([[<M-j>]], [[<C-\><C-n><C-w>j]])
tnoremap ([[<M-k>]], [[<C-\><C-n><C-w>k]])
tnoremap ([[<M-l>]], [[<C-\><C-n><C-w>l]])

-- resize splits left/right
noremap ([[<M-[>]], [[<C-w>3<]])
noremap ([[<M-]>]], [[<C-w>3>]])

-- resize splits up/down
noremap ([[<M-{>]], [[<C-w>3-]])
noremap ([[<M-}>]], [[<C-w>3+]])

